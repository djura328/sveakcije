    public function menu($elements, $parentId = 0)
    {
        $elements = Category::where('perent', $parentId)->get();
        $branch = array();
        foreach ($elements as $element) {
            if ($element['perent'] == $parentId) {
                $children = $this->menu($elements, $element['id']);
                if ($children) {
                    $element['children'] = $children;
                }
                $branch[] = $element;
            }
        }
        return $branch;
    }
